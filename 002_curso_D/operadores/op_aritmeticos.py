# suma y resta
suma = 12 + 5
print(f'la sumas es >> {suma}')
resta = 12 -5
print(f'la resta es >> {resta}')

# multiplicaion y division
multiplicacion = 12 * 5
print(f'la multiplicaion es >> {multiplicacion}')
division = 12 / 5 # devuelve un dato flotante
print(f'la division es >> {division}')

# potencia (exponenete) (si signo es **)
exponente = 12 ** 5
print(f'la potencia es >> {exponente}')

# division baja (//)
division_baja = 12 // 5 # devuenve un entero redondeado hacia abajo
print(f'la division baja es >> {division_baja}')

# modulo
modulo = 15 % 5
print(f'el modulo es >> {modulo}') 